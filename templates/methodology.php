    <h2>Чего этот бенчмарк не показывает</h2>
    <ul>
      <li>Запись файлов — сравниваются только читатели.</li>
      <li>Работу со стилями, формулами, изображениями, диаграммами и объединёнными ячейками.
        PhpSpreadsheet умеет несопоставимо больше остальных, и в задачах, где нужна полная модель
        документа, у него просто нет конкурентов в этом списке.</li>
      <li>Форматы, кроме XLSX/XLSM: CSV, ODS и старый XLS сюда не входят.</li>
      <li>Поведение под нагрузкой и в конкурентном режиме — все замеры последовательные.</li>
