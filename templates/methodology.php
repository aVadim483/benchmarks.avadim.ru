      только размеры, счётчики и тайминги. Отчёты хранятся
      <?= (int) $settings['web']['retention_hours'] ?> ч, затем удаляются.
      Ограничения: до <?= Format::bytes((int) $settings['web']['max_upload_bytes']) ?> на файл и
